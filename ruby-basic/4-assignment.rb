#ruby的赋值方式


# 多重赋值
a,b,c = 1,2,3           #"a:1  b:2  c:3"
a,b,*c = 1,2,3,4,5,6    #"a:1  b:2  c:[3, 4, 5, 6]"
a,*b,c = 1,2,3,4,5,6    #"a:1  b:[2, 3, 4, 5]  c:6"
*a,b,c = 1,2,3,4,5,6    #"a:[1, 2, 3, 4]  b:5  c:6"

p "a:#{a}  b:#{b}  c:#{c}"


# 交换赋值
c = 'ccc'
d = 'ddd'

c,d = d,c 

p "c:#{c}  d:#{d}"  #"c:ddd  d:ccc"


# 数组赋值
arr = [1,2,3,4]
e,f,g,h = arr

p "e:#{e}  f:#{f}  g:#{g}  h:#{h}"  #"e:1  f:2  g:3  h:4"